    // ===== MUSIC (chiptune generated with Web Audio API) =====
    // Safari/iOS requires AudioContext to be created AND resumed inside a user gesture.
    // We unlock audio on the very first user interaction.
    function initAudio() {
        if (audioCtx) {
            // Always try to resume on user gesture (Safari requires this)
            if (audioCtx.state === 'suspended') {
                audioCtx.resume();
            }
            return;
        }
        try {
            audioCtx = new (window.AudioContext || window.webkitAudioContext)();
            // Immediately resume (needed for Safari)
            if (audioCtx.state === 'suspended') {
                audioCtx.resume();
            }
            // Play a silent buffer to fully unlock audio on iOS Safari
            const silentBuffer = audioCtx.createBuffer(1, 1, 22050);
            const silentSource = audioCtx.createBufferSource();
            silentSource.buffer = silentBuffer;
            silentSource.connect(audioCtx.destination);
            silentSource.start(0);
        } catch (e) {
            audioCtx = null;
        }
    }

    // Unlock audio on first touch/click (iOS Safari requirement)
    function unlockAudio() {
        initAudio();
        document.removeEventListener('touchstart', unlockAudio);
        document.removeEventListener('touchend', unlockAudio);
        document.removeEventListener('click', unlockAudio);
    }
    document.addEventListener('touchstart', unlockAudio, { once: true });
    document.addEventListener('touchend', unlockAudio, { once: true });
    document.addEventListener('click', unlockAudio, { once: true });

    function playChiptune() {
        if (!audioCtx) initAudio();
        if (!audioCtx) return; // Audio not available
        if (audioCtx.state === 'suspended') audioCtx.resume();

        // Simple happy melody
        const melody = [
            { note: 523, dur: 0.2 }, // C5
            { note: 587, dur: 0.2 }, // D5
            { note: 659, dur: 0.2 }, // E5
            { note: 523, dur: 0.2 }, // C5
            { note: 659, dur: 0.3 }, // E5
            { note: 784, dur: 0.4 }, // G5
            { note: 784, dur: 0.4 }, // G5
            { note: 698, dur: 0.2 }, // F5
            { note: 659, dur: 0.2 }, // E5
            { note: 587, dur: 0.2 }, // D5
            { note: 523, dur: 0.2 }, // C5
            { note: 587, dur: 0.3 }, // D5
            { note: 523, dur: 0.3 }, // C5
            { note: 440, dur: 0.4 }, // A4
            { note: 523, dur: 0.4 }, // C5
        ];

        let time = audioCtx.currentTime;
        melody.forEach(({ note, dur }) => {
            const osc = audioCtx.createOscillator();
            const gain = audioCtx.createGain();
            osc.type = 'square';
            osc.frequency.value = note;
            gain.gain.setValueAtTime(0.08, time);
            gain.gain.exponentialRampToValueAtTime(0.001, time + dur - 0.02);
            osc.connect(gain);
            gain.connect(audioCtx.destination);
            osc.start(time);
            osc.stop(time + dur);
            time += dur;
        });

        // Loop it
        if (musicPlaying) {
            const totalDur = melody.reduce((sum, n) => sum + n.dur, 0);
            setTimeout(() => {
                if (musicPlaying) playChiptune();
            }, totalDur * 1000 + 500);
        }
    }

