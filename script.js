    // ===== FLOATING PARTICLE SYSTEM (Ghibli atmosphere) =====
    const particleTypes = ['seed', 'seed', 'seed', 'leaf', 'firefly'];
    const particles = [];
    const MAX_PARTICLES = 15;

    function createParticle() {
        if (!gameStarted) return;
        if (particles.length >= MAX_PARTICLES) return;

        const type = particleTypes[Math.floor(Math.random() * particleTypes.length)];
        const el = document.createElement('div');
        el.className = `particle particle-${type}`;

        const startX = cameraX + Math.random() * viewportWidth;
        const startY = window.innerHeight * (0.1 + Math.random() * 0.5);

        el.style.left = startX + 'px';
        el.style.top = startY + 'px';

        ground.appendChild(el);

        const particle = {
            el,
            x: startX,
            y: startY,
            vx: 0.3 + Math.random() * 0.6,
            vy: type === 'firefly' ? (Math.random() - 0.5) * 0.3 : 0.1 + Math.random() * 0.3,
            wobble: Math.random() * Math.PI * 2,
            wobbleSpeed: 0.02 + Math.random() * 0.03,
            wobbleAmp: type === 'firefly' ? 15 + Math.random() * 20 : 8 + Math.random() * 12,
            life: 0,
            maxLife: 300 + Math.random() * 400,
            type
        };
        particles.push(particle);
    }

    function updateParticles() {
        for (let i = particles.length - 1; i >= 0; i--) {
            const p = particles[i];
            p.life++;
            p.wobble += p.wobbleSpeed;

            p.x += p.vx;
            p.y += p.vy + Math.sin(p.wobble) * 0.3;

            const wobbleOffset = Math.sin(p.wobble) * p.wobbleAmp;
            const opacity = p.life < 30 ? p.life / 30 :
                            p.life > p.maxLife - 60 ? (p.maxLife - p.life) / 60 : 1;

            p.el.style.transform = `translate(${wobbleOffset}px, 0) rotate(${p.wobble * 20}deg)`;
            p.el.style.opacity = opacity;
            p.el.style.left = p.x + 'px';
            p.el.style.top = p.y + 'px';

            if (p.life >= p.maxLife) {
                p.el.remove();
                particles.splice(i, 1);
            }
        }
    }

